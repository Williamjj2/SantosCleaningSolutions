// Dynamic posts loaded from Supabase via /api/blog (updated by N8N auto-publisher)
let dynamicPosts: BlogPost[] = [];
let dynamicLoaded = false;

export async function loadDynamicPosts(): Promise<BlogPost[]> {
  if (dynamicLoaded) return getAllPosts();
  try {
    const res = await fetch("/api/blog");
    if (res.ok) {
      const data = await res.json();
      const posts: BlogPost[] = (data.posts || []).map((p: any) => ({
        slug: p.slug,
        title: p.title,
        description: p.description || "",
        image: p.image_url || "/images/blog/default.jpg",
        date: new Date(p.publish_date).toISOString().split("T")[0],
        location: p.target_city ? `${p.target_city}, GA` : "Atlanta, GA",
        category: p.category || "Cleaning Tips",
        readTime: p.read_time || "5 min read",
      }));
      const staticSlugs = new Set(blogPosts.map((p) => p.slug));
      dynamicPosts = posts.filter((p) => !staticSlugs.has(p.slug));
      dynamicLoaded = true;
    }
  } catch {
    // Silently fail — static posts are the fallback
  }
  return getAllPosts();
}

export function getAllPosts(): BlogPost[] {
  return [...dynamicPosts, ...blogPosts].sort(
    (a, b) => new Date(b.date).getTime() - new Date(a.date).getTime()
  );
}

export function getBlogPost(slug: string): BlogPost | undefined {
  return getAllPosts().find((p) => p.slug === slug);
}

export function getRelatedPosts(currentSlug: string, limit = 3): BlogPost[] {
  const all = getAllPosts();
  const current = getBlogPost(currentSlug);
  if (!current) return all.slice(0, limit);

  return all
    .filter((p) => p.slug !== currentSlug)
    .sort((a, b) => {
      const aMatch =
        a.location === current.location
          ? 2
          : a.category === current.category
            ? 1
            : 0;
      const bMatch =
        b.location === current.location
          ? 2
          : b.category === current.category
            ? 1
            : 0;
      return bMatch - aMatch;
    })
    .slice(0, limit);
}

export function getBlogCategories(): string[] {
  return [
    "All",
    ...Array.from(new Set(getAllPosts().map((p) => p.category))),
  ];
}

